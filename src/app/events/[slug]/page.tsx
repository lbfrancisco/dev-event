import { BookEvent } from '@/components/book-event'
import { EventCard } from '@/components/event-card'
import type { IEvent } from '@/database'
import { getSimilarEventsBySlug } from '@/lib/actions/event.actions'
import { cacheLife } from 'next/cache'
import Image from 'next/image'
import { notFound } from 'next/navigation'

interface EventDetailsParams {
	params: Promise<{
		slug: string
	}>
}

const BASE_URL = process.env.NEXT_PUBLIC_BASE_URL

export function EventDetailItem({
	icon,
	alt,
	label,
}: {
	icon: string
	alt: string
	label: string
}) {
	return (
		<div className="flex-row-gap-2 items-center">
			<Image src={icon} alt={alt} width={17} height={17} />
			<p>{label}</p>
		</div>
	)
}

export function EventAgenda({ agendaItems }: { agendaItems: string[] }) {
	return (
		<div className="agenda">
			<h2>Agenda</h2>
			<ul>
				{agendaItems.map((agenda) => (
					<li key={agenda}>{agenda}</li>
				))}
			</ul>
		</div>
	)
}

export function EventTags({ tags }: { tags: string[] }) {
	return (
		<div className="flex flex-row gap-1.5 flex-wrap">
			{tags.map((tag) => (
				<div className="pill" key={tag}>
					{tag}
				</div>
			))}
		</div>
	)
}

export default async function EventDetails({ params }: EventDetailsParams) {
	'use cache'
	cacheLife('hours')

	const { slug } = await params

	const request = await fetch(`${BASE_URL}/api/events/${slug}`)
	const { event } = await request.json()

	if (!event) return notFound()

	const {
		_id: id,
		title,
		description,
		image,
		overview,
		date,
		time,
		location,
		mode,
		agenda,
		audience,
		tags,
		organizer,
	} = event

	const bookings = 0

	const similarEvents: IEvent[] = await getSimilarEventsBySlug(slug)

	return (
		<section id="event">
			<div className="header">
				<h1>{title}</h1>
				<p>{description}</p>
			</div>
			<div className="details">
				<div className="content">
					<Image
						src={image}
						alt="Event Banner"
						width={800}
						height={800}
						className="banner"
					/>

					<section className="flex-col-gap-2">
						<h2>Overview</h2>
						<p>{overview}</p>
					</section>
					<section className="flex-col-gap-2">
						<h2>Event Details</h2>
						<EventDetailItem
							icon="/icons/calendar.svg"
							alt="calendar"
							label={date}
						/>
						<EventDetailItem icon="/icons/clock.svg" alt="clock" label={time} />
						<EventDetailItem icon="/icons/pin.svg" alt="pin" label={location} />
						<EventDetailItem icon="/icons/mode.svg" alt="mode" label={mode} />
						<EventDetailItem
							icon="/icons/audience.svg"
							alt="audience"
							label={audience}
						/>
					</section>

					<EventAgenda agendaItems={agenda} />

					<section className="flex-col-gap-2">
						<h2>About the Organizer</h2>
						<p>{organizer}</p>
					</section>

					<EventTags tags={tags} />
				</div>
				<aside className="booking">
					<div className="signup-card">
						<h2>Book Your Spot</h2>
						{bookings > 0 ? (
							<p className="text-sm">
								Join {bookings} people who have already booked their spot!
							</p>
						) : (
							<p className="text-sm">Be the first to book your spot!</p>
						)}
						<BookEvent eventId={id} slug={slug} />
					</div>
				</aside>
			</div>

			<div className="flex w-full flex-col gap-4 pt-20">
				<h2>Similar Events</h2>
				<div className="events">
					{similarEvents &&
						similarEvents.length > 0 &&
						similarEvents.map((similarEvent: IEvent) => (
							<EventCard key={similarEvent.slug} {...similarEvent} />
						))}
				</div>
			</div>
		</section>
	)
}
